string_t my_strncat(string_t dest, cstring_t src, uint_t n)
{
    ulong_t dlen = my_strlen(dest);
    uint_t i = 0;

    for (; i < n && src[i]; i++)
        dest[dlen + i] = src[i];
    dest[dlen + i] = '\0';
    return (dest);
}
