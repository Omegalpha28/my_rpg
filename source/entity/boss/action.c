void boss_action(entity_t *boss)
{
    if (boss->actor->self->id == CREATURE_CRAB_BOSS)
        crab_attack(boss);
    if (boss->actor->self->id == CTHULU_TODO)
        cthulu_action(boss);
    return;
}
