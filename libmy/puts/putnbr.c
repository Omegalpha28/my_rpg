void my_putnbr(int nbr)
{
    if (nbr < 0) {
        nbr *= -1;
        my_putchar('-');
    }
    if (nbr >= 10)
        my_putnbr(nbr / 10);
    my_putchar(nbr % 10 + '0');
}
