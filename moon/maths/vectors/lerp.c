v2f_t lerp2f(v2f_t a, v2f_t b, float t)
{
    return ((v2f_t){
        a.x + (b.x - a.x) * t,
        a.y + (b.y - a.y) * t});
}

///////////////////////////////////////////////////////////////////////////////
v2i_t lerp2i(v2i_t a, v2i_t b, float t)
{
    return ((v2i_t){
        a.x + (b.x - a.x) * t,
        a.y + (b.y - a.y) * t});
}

///////////////////////////////////////////////////////////////////////////////
v2u_t lerp2u(v2u_t a, v2u_t b, float t)
{
    return ((v2u_t){
        a.x + (b.x - a.x) * t,
        a.y + (b.y - a.y) * t});
}

///////////////////////////////////////////////////////////////////////////////
v3f_t lerp3f(v3f_t a, v3f_t b, float t)
{
    return ((v3f_t){
        a.x + (b.x - a.x) * t,
        a.y + (b.y - a.y) * t,
        a.z + (b.z - a.z) * t});
}
