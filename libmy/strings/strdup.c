string_t my_strdup(cstring_t str)
{
    ulong_t len = my_strlen(str) + 1;
    string_t copy;

    copy = malloc((uint_t)len);
    RETURN(!copy, NULL);
    my_memcpy(copy, str, len);
    return (copy);
}
