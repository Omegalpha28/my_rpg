string_t my_strpbrk(cstring_t s, cstring_t accept)
{
    cstring_t a;

    for (; *s; s++) {
        a = accept;
        for (; *a; a++)
            RETURN(*a == *s, (string_t)s);
    }
    return (NULL);
}
