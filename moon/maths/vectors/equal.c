bool_t equal2f(v2f_t a, v2f_t b)
{
    return (BOOL((a.x == b.x) && (a.y == b.y)));
}

///////////////////////////////////////////////////////////////////////////////
bool_t equal2u(v2u_t a, v2u_t b)
{
    return (BOOL((a.x == b.x) && (a.y == b.y)));
}

///////////////////////////////////////////////////////////////////////////////
bool_t equal2i(v2i_t a, v2i_t b)
{
    return (BOOL((a.x == b.x) && (a.y == b.y)));
}

///////////////////////////////////////////////////////////////////////////////
bool_t equal3f(v3f_t a, v3f_t b)
{
    return (BOOL((a.x == b.x) && (a.y == b.y) && (a.z == b.z)));
}
