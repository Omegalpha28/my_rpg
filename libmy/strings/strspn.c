ulong my_strspn(cstring s1, cstring s2)
{
    cstring s = s1;
    cstring c;

    while (*s1) {
        for (c = s2; *c && !(*s1 == *c); c++);
        if (*c == '\0')
            break;
        s1++;
    }
    return (s1 - s);
}
