///////////////////////////////////////////////////////////////////////////////
static void crab_attack(entity_t *crab)
{
    if (crab->status == Patrol)
        mouth_blast(crab);
    if (crab->status == Dazed)
        return;
    if (crab->status == Agressive)
        spin_dash(crab);
    if (crab->status == Fear)
        bubble_defense(crab);
}

