v2f_t divide2f(v2f_t a, v2f_t b)
{
    v2f_t r = {0.0f, 0.0f};

    if (b.x != 0.0f)
        r.x = a.x / b.x;
    if (b.y != 0.0f)
        r.y = a.y / b.y;
    return (r);
}

///////////////////////////////////////////////////////////////////////////////
v2i_t divide2i(v2i_t a, v2i_t b)
{
    v2i_t r = {0, 0};

    if (b.x != 0)
        r.x = a.x / b.x;
    if (b.y != 0)
        r.y = a.y / b.y;
    return (r);
}

///////////////////////////////////////////////////////////////////////////////
v2u_t divide2u(v2u_t a, v2u_t b)
{
    v2u_t r = {0u, 0u};

    if (b.x != 0u)
        r.x = a.x / b.x;
    if (b.y != 0u)
        r.y = a.y / b.y;
    return (r);
}

///////////////////////////////////////////////////////////////////////////////
v3f_t divide3f(v3f_t a, v3f_t b)
{
    v3f_t r = {0.0f, 0.0f, 0.0f};

    if (b.x != 0.0f)
        r.x = a.x / b.x;
    if (b.y != 0.0f)
        r.y = a.y / b.y;
    if (b.z != 0.0f)
        r.z = a.z / b.z;
    return (r);
}
