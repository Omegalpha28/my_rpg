float dot2f(v2f_t a, v2f_t b)
{
    return ((a.x * b.x) + (a.y * b.y));
}

///////////////////////////////////////////////////////////////////////////////
uint_t dot2u(v2u_t a, v2u_t b)
{
    return ((a.x * b.x) + (a.y * b.y));
}

///////////////////////////////////////////////////////////////////////////////
int dot2i(v2i_t a, v2i_t b)
{
    return ((a.x * b.x) + (a.y * b.y));
}

///////////////////////////////////////////////////////////////////////////////
float dot3f(v3f_t a, v3f_t b)
{
    return ((a.x * b.x) + (a.y * b.y) + (a.z * b.z));
}
