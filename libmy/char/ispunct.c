bool_t my_ispunct(int ch)
{
    return (BOOL((ch == '!' || (ch >= '"' && ch <= '&')
        || (ch >= '(' && ch <= '/') || (ch >= ':' && ch <= '?')
        || (ch >= '[' && ch <= '`') || (ch >= '{' && ch <= '~'))));
}
