buffer_t *my_buffinit(void)
{
    buffer_t *buffer = (buffer_t *)malloc(sizeof(buffer_t));

    if (buffer == NULL)
        return (NULL);
    buffer->content = NULL;
    buffer->size = 0;
    return (buffer);
}
